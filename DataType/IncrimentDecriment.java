
package DataType;
public class IncrimentDecriment {
    public static void main(String[] args) {
        int x = 20;
        int y;
        y = x++;
        System.out.println("y :"+y);
        y = ++x;
        System.out.println("y :"+y);
        y = x--;
        System.out.println("y :"+y);
        y = --x;
        System.out.println("y :"+y);
    }
    
}
